        }

        $object = new Guiatransferencias($id);

        $row = $list->datagrid->addItem($object);
        $row->id = "row_{$object->idguiatransferencias}";

        if($openTransaction)
        {
            TTransaction::close();    
        }

        TDataGrid::replaceRowById(__CLASS__.'_datagrid', $row->id, $row);
    }

